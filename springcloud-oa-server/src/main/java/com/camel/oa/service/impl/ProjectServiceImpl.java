@Service
public class ProjectServiceImpl extends ServiceImpl<ProjectMapper, Project> implements ProjectService {

    @Autowired
    private ProjectMapper mapper;

    @Autowired
    private RedisTemplate redisTemplate;

    @Override
    public PageInfo<Project> selectPage(Project entity) {
        PageInfo pageInfo = PaginationUtil.startPage(entity, () -> {
            mapper.list(entity);
        });
        return pageInfo;
    }

    @Override
    public Result save(Project entity, OAuth2Authentication authentication) {
        entity.setStatus(ProjectStatus.NORMAL);
        entity.setCode(UUID.randomUUID().toString());
        Member member = (Member) SessionContextUtils.getInstance().currentUser(redisTemplate, authentication.getName());
        entity.setCreator(new SysUser(member.getId()));
        entity.setCreatorId(member.getId());
        if (mapper.insert(entity) > -1) {
            return ResultUtil.success("新增成功");
        }
        return ResultUtil.error(HttpStatus.INTERNAL_SERVER_ERROR.value(), "新增失败");
    }

    @Override
    public Result release(Integer id) {
        Integer result = mapper.updateById(new Project(id, ProjectStatus.RELEASE));
        if (result > -1) {
            return ResultUtil.success("发布成功");
        }
        return ResultUtil.error(HttpStatus.INTERNAL_SERVER_ERROR.value(), "发布失败");
    }
}
